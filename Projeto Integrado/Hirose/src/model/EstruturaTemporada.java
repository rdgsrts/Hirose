package model;

public class EstruturaTemporada 
{
	private int codTemporada, codFase, codGrupo, codRodada;
	
	private String nomeFase, nomeGrupo, nomeRodada, tipoEstrutura;

	public int getCodTemporada() {
		return codTemporada;
	}

	public void setCodTemporada(int codTemporada) {
		this.codTemporada = codTemporada;
	}

	public int getCodFase() {
		return codFase;
	}

	public void setCodFase(int codFase) {
		this.codFase = codFase;
	}

	public int getCodGrupo() {
		return codGrupo;
	}

	public void setCodGrupo(int codGrupo) {
		this.codGrupo = codGrupo;
	}

	public int getCodRodada() {
		return codRodada;
	}

	public void setCodRodada(int codRodada) {
		this.codRodada = codRodada;
	}

	public String getNomeFase() {
		return nomeFase;
	}

	public void setNomeFase(String nomeFase) {
		this.nomeFase = nomeFase;
	}

	public String getNomeGrupo() {
		return nomeGrupo;
	}

	public void setNomeGrupo(String nomeGrupo) {
		this.nomeGrupo = nomeGrupo;
	}

	public String getNomeRodada() {
		return nomeRodada;
	}

	public void setNomeRodada(String nomeRodada) {
		this.nomeRodada = nomeRodada;
	}

	public String getTipoEstrutura() {
		return tipoEstrutura;
	}

	public void setTipoEstrutura(String tipoEstrutura) {
		this.tipoEstrutura = tipoEstrutura;
	}
	
	
}
